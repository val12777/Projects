package Conditions;



public class Conditions {
    public static void main(String[] args) {

        int count = 250;
        if (count < 100) {
            System.out.println("Мало");
        } else if (count < 200) {
            System.out.println("Средне");
        }
         else {
            System.out.println("Много");
        }



    }
}